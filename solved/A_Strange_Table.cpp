void solve()
{
    ll n, m, x;
    cin >> n >> m >> x;
    ll col = ceil((double)x / n);
    ll row = x - ((col - 1) * n);
    //cout<<row<<" "<<col<<" ";
    cout << ((row - 1) * m) + col << endl;
}

int main()
{
    ios_base::sync_with_stdio(false);
    cin.tie(NULL);
    ll tt;
    cin >> tt;
    while (tt--)
        solve();

    return 0;
}